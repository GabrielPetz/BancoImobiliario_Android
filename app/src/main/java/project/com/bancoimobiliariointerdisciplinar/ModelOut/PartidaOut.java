package project.com.bancoimobiliariointerdisciplinar.ModelOut;

public class PartidaOut {
    private Integer parid;
    private String parnome;
    private String pardata;
    private Integer parstatus;

    public PartidaOut() {
    }

    public Integer getParid() {
        return parid;
    }

    public void setParid(Integer parid) {
        this.parid = parid;
    }

    public String getParnome() {
        return parnome;
    }

    public void setParnome(String parnome) {
        this.parnome = parnome;
    }

    public String getPardata() {
        return pardata;
    }

    public void setPardata(String pardata) {
        this.pardata = pardata;
    }

    public Integer getParstatus() {
        return parstatus;
    }

    public void setParstatus(Integer parstatus) {
        this.parstatus = parstatus;
    }
}
